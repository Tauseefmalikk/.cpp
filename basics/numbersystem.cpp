#include<iostream>
int main()
{
    int n1=15;            //decimal
    int n2=017;          //octal
    int n3=0x0f;        //hexadecimal
    int n4=0b0001111;  //binary

    std::cout<<"n1="<<n1<<std::endl;
    std::cout<<"n2="<<n2<<std::endl;
    std::cout<<"n3="<<n3<<std::endl;
    std::cout<<"n4="<<n4<<std::endl;

    return 0;
    
}